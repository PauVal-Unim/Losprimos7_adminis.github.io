<!-- PREGUNTA 1: ¿Cómo aplica cada fase del proceso administrativo? -->
<!-- ========================================================================= -->
<div class="card mb-5 shadow-lg p-4 bg-light">
    <h2 class="h4 text-center text-dark mb-4">1. ¿Cómo aplica la empresa seleccionada cada fase del proceso administrativo?</h2>
    <hr class="mb-4">
    <p class="mb-4">Al aplicar los conceptos de Koontz, Weihrich, y Chiavenato a Los Primos 7, comprendimos que:</p>
    
    <!-- Planeación -->
    <h3 class="h6 text-primary fw-bold">Planeación</h3>
    <p>Aunque no se ha detallado en los documentos, la planeación se evidencia en la existencia de metas mensuales de volumen de chatarra procesada y el enfoque en la transparencia y eficiencia como objetivos centrales. La existencia de la meta de volumen establece un plan cuantitativo que guía las operaciones diarias y el sistema de incentivos.</p>
    
    <!-- Organización -->
    <h3 class="h6 text-primary fw-bold mt-4">Organización</h3>
    <ul class="list-unstyled">
        <li>La empresa utiliza una Estructura Organizacional Funcional Simple. Esta estructura agrupa las tareas por especialidad (Administrativo, Operaciones, Compras), lo cual es apropiado para su tamaño y el control centralizado por parte de la Gerencia General (Fundadores).</li>
        <li>Jerarquía: La estructura es clara:   
            <ul class="ms-4">
                <li>Nivel Superior: Gerencia General.</li>
                <li>Nivel Operativo Alto: Coordinador Operativo y Área Administrativa y Comercial.</li>
                <li>Nivel Operativo Bajo: Personal clave como Encargado de Recepción y Pesaje, Asistente Contable, y Encargado de Compras y Ventas.</li>
            </ul>
        </li>
        <li>Decisión Evidenciada:La separación de funciones clave como Logística y Compras/Ventas garantiza que cada área se centre en sus objetivos específicos (eficiencia de rutas vs. negociación de precios).</li>
    </ul>
    
    <!-- Dirección -->
    <h3 class="h6 text-primary fw-bold mt-4">Dirección</h3>
    <ul class="list-unstyled">
        <li>Se aplica un estilo de Liderazgo Transaccional. Se basa en la claridad de roles y el uso de recompensas (bonos por volumen de chatarra, pago de horas extras) para motivar el cumplimiento de los procesos y metas.</li>
        <li>Comunicación:Se implementa un Plan de Comunicación:    
            <ul class="ms-4">
                <li>Vertical (Descendente): Uso de reuniones matutinas (briefings) para asignar rutas y metas diarias, garantizando que las decisiones gerenciales lleguen al personal operativo.</li>
                <li>Horizontal: Uso de grupos de mensajería (WhatsApp) para coordinar la logística y la capacidad de recepción entre las 3 sucursales, vital para un negocio multisede.</li>
            </ul>
        </li>
        <li>Motivación: Se combina la estabilidad financiera con el reconocimiento moral: Incentivos monetarios y el programa "Empleado del Mes" asociado a un KPI clave (cero errores de pesaje).</li>
    </ul>
    
    <!-- Control -->
    <h3 class="h6 text-primary fw-bold mt-4">Control</h3>
    <ul class="list-unstyled">
        <li>Vínculo con Dirección:El control está directamente ligado a la fase de Dirección a través del sistema de recompensas. La Gerencia establece un estándar y ofrece un incentivo si se alcanza o se supera.</li>
        <li>Medición: La evaluación de la gestión se realiza mediante los KPI y métricas definidos.</li>
        <li>Estrategia Clave: La medición de "cero errores de pesaje" es una estrategia de control de calidad fundamental que busca la transparencia, un valor central de la empresa.</li>
    </ul>
</div>

